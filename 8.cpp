#include<iostream>
#include<vector>
#include <algorithm>
using namespace std;

int main() {
    string s;
    cin >> s;
    int n = s.length();
    vector<char> res;

    for(int i = 0; i < n; i = i + 2) {
        res.push_back(s[i]);
    }
    sort(res.begin(), res.end());
    for(int i = 0; i < n; i = i + 2) {
        s[i] = res[i/2]; // because here + is not present 
    }
    cout << s;
    return 0;
}
